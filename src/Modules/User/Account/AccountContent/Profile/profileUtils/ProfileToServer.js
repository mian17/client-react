export default class ProfileToServer {
  constructor(
    username,
    name,
    email,
    phone_number,
    gender,
    birth_date,
    address
  ) {
    this.username = username;
    this.name = name;
    this.email = email;
    this.phone_number = phone_number;
    this.gender = gender;
    this.birth_date = birth_date;
    this.address = address;
  }
}
